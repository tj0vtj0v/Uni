    /**
     * Generates a Blockimage of a sand sack wall.
     *
     * @param segments middle segments of the wall.
     * @return a string containing the whole wall.
     */
    public String wall(int segments) {
        String[] leftEnd = {
                "       ",
                "      7",
                "      7",
                "    777",
                "   7117",
                "   7111",
                " 777111",
                "7117111",
                "7111777",
                "7111117",
                "7111117",
                "7777771",
                " 711111",
                " 711111",
                "  77777",
        };

        String[] segment = {
                "77777 ",
                "111117",
                "111117",
                "111117",
                "111117",
                "777771",
                "117111",
                "117111",
                "771777",
                "111117",
                "111117",
                "777771",
                "711111",
                "711111",
                " 77777",
        };

        String[] rightEnd = {
                "       ",
                "       ",
                "       ",
                "77     ",
                "117    ",
                "117    ",
                "11777  ",
                "117117 ",
                "771117 ",
                "111117 ",
                "111117 ",
                "7777717",
                "1711117",
                "1711117",
                "7 7777 ",
        };

        StringBuilder result = new StringBuilder();

        for (int index = 0; index < segment.length; index++) {
            result.append(leftEnd[index]).append(segment[index].repeat(segments)).append(rightEnd[index]).append("\n");
        }

        return result.toString();
    }
