package thd.game.level;

/**
 * Enum representation of the Difficulties.
 */
public enum Difficulty {
    /**
     * Difficulty mode Easy -> you have to win.
     */
    EASY,
    /**
     * Difficulty mode Standard -> you most likely win, but it's not too easy.
     */
    STANDARD,
    /**
     * Difficulty mode Hard -> you will die a lot of times and be relieved when you close the game.
     */
    HARD,
    /**
     * Difficulty mode Impossible -> you will die and start from the beginning.
     */
    IMPOSSIBLE
}
