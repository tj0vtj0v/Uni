package loops;

public class Loops {
    public static void main(String[] args) {

        for (int i=5; i<=20; i+=3) {
            System.out.print(i + " ");
        }

        System.out.println();
        for (int i=70; i<=210; i+=35) {
            System.out.print(i + " ");
        }

        System.out.println();
        for (int i=47; i>=17; i-=6) {
            System.out.print(i + " ");
        }
    }
}
